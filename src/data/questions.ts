// ========== 维度 2/3/4 通用题目 (27 题，每维 9 题) ==========

function q(id: string, dim: DimId, text: string, opts: [string, number][]) {
    return { id, dim, text, options: opts.map(([label, value]) => ({ label, value })) };
}

function r(lo: string, loVal: number, mid: string, midVal: number, hi: string, hiVal: number) {
    return [
        [lo, loVal] as [string, number],
        [mid, midVal] as [string, number],
        [hi, hiVal] as [string, number],
    ];
}

